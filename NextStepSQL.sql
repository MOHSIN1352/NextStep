CREATE TABLE Users (
    User_ID SERIAL PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    Email VARCHAR(255) UNIQUE NOT NULL,
    Password VARCHAR(255) NOT NULL,
    Gender VARCHAR(10),
    City_Name VARCHAR(255),
    State_Name VARCHAR(255),
    Date_Of_Birth DATE
);

CREATE TABLE Employer (
    Employer_ID SERIAL PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    Location VARCHAR(255),
    Average_Salary INT,
    Website TEXT
);


CREATE TABLE Institute (
    Institution_ID SERIAL PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    Tuition_Fees INT,
    Address TEXT,
    Website VARCHAR(255),
    Accreditation_Status BOOLEAN DEFAULT FALSE,
    Established_Year INT,
    Enrollment_Link TEXT
);

CREATE TABLE Government_Policy (
    Policy_ID SERIAL PRIMARY KEY,
    Policy_Name VARCHAR(255) NOT NULL,
    Policy_Type VARCHAR(255),
    Policy_Region VARCHAR(255)
);

CREATE TABLE Health_Facility (
    Facility_ID SERIAL PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    Facility_Type VARCHAR(255),
    Bed_Capacity INT,
    Facility_Region VARCHAR(255)
);

CREATE TABLE Job (
    Job_ID SERIAL PRIMARY KEY,
    Employer_ID INT REFERENCES Employer(Employer_ID) ON DELETE CASCADE,
    Title VARCHAR(255) NOT NULL,
    Industry_Type VARCHAR(255),
    Salary INT,
    Location VARCHAR(255),
    Job_Description TEXT
);

CREATE TABLE Online_Courses (
    Course_ID SERIAL PRIMARY KEY,
    Name TEXT NOT NULL,
    Platform TEXT NOT NULL,
    Category VARCHAR(100),
    URL TEXT UNIQUE NOT NULL,
    Duration TEXT,
    Fees DECIMAL(10,2) DEFAULT NULL,
    Certification BOOLEAN DEFAULT FALSE,
    Rating DECIMAL(3,2) CHECK (Rating BETWEEN 0 AND 5)
);


